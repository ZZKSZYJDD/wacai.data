    public void setBloggerName(String bloggerName) {
        this.bloggerName = bloggerName;
    }

    public String getBloggerTel() {
        return bloggerTel;
    }

    public void setBloggerTel(String bloggerTel) {
        this.bloggerTel = bloggerTel;
    }

    public String getBloggerPwd() {
        return bloggerPwd;
    }

    public void setBloggerPwd(String bloggerPwd) {
        this.bloggerPwd = bloggerPwd;
    }

    public String getBloggerSex() {
        return bloggerSex;
    }

    public void setBloggerSex(String bloggerSex) {
        this.bloggerSex = bloggerSex;
    }
